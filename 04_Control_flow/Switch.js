const month = "March"

switch (month) {
    case "jan":
        console.log('January');
        break;
    case "feb":
        console.log('February');
        break;
    case "March":
        console.log('March');
        break;
    case "april":
        console.log('April');
        break;

    default:
        console.log('Invalid Month');
        break;
}